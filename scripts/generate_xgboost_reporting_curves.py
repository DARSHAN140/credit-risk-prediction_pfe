"""Generate XGBoost presentation figures from fixed aggregate metrics.

These figures are reconstructions for reporting only.  They are not empirical
curves calculated from individual model predictions.  Real test curves remain
generated by ``generate_real_auc_ks_threshold_curves.py``.
"""

from __future__ import annotations

import argparse
import json
import os
from pathlib import Path
import sys

import matplotlib
import numpy as np
import pandas as pd
from sklearn.metrics import auc


BASE_DIR = Path(__file__).resolve().parents[1]
if str(BASE_DIR) not in sys.path:
    sys.path.insert(0, str(BASE_DIR))

MPL_CACHE_DIR = BASE_DIR / ".matplotlib_cache"
MPL_CACHE_DIR.mkdir(exist_ok=True)
os.environ.setdefault("MPLCONFIGDIR", str(MPL_CACHE_DIR))
matplotlib.use("Agg")
import matplotlib.pyplot as plt

from src.evaluation import confusion_matrix_from_reported_metrics


IMG_DIR = BASE_DIR / "img" / "reporting_xgboost"
MAIN_IMG_DIR = BASE_DIR / "img"
DATA_DIR = BASE_DIR / "reports" / "reporting_xgboost"
REPLACE_MAIN_ASSETS = False

DARK = "#1f2a35"
GRAY = "#66727f"
GRID = "#d8dee4"
BLUE = "#2f5597"
ORANGE = "#c77c2b"
GREEN = "#4f7d5a"
RED = "#b04a4a"
PURPLE = "#6b4fa3"

METRICS = {
    "accuracy": 0.884,
    "precision": 0.879,
    "recall": 0.862,
    "f1": 0.870,
    "roc_auc": 0.890,
    "gini": 0.780,
    "ks": 0.665,
    "pr_auc": 0.881,
    "brier_score": 0.095,
    "ece": 0.021,
    "threshold": 0.500,
}

METRICS_TEXT = (
    "Accuracy 88.4% | Precision 87.9% | Recall 86.2% | F1 87.0%\n"
    "ROC-AUC 89.0% | Gini 78.0% | KS 66.5% | PR-AUC 88.1% | "
    "Brier 0.095 | ECE 0.021"
)


def add_metrics_box(ax: plt.Axes, location: str = "upper right") -> None:
    positions = {
        "upper right": (0.985, 0.975, "right", "top"),
        "upper left": (0.015, 0.975, "left", "top"),
        "lower right": (0.985, 0.025, "right", "bottom"),
        "lower left": (0.015, 0.025, "left", "bottom"),
    }
    x, y, horizontal, vertical = positions[location]
    ax.text(
        x,
        y,
        METRICS_TEXT,
        transform=ax.transAxes,
        ha=horizontal,
        va=vertical,
        fontsize=8.6,
        color=DARK,
        bbox={
            "boxstyle": "round,pad=0.45",
            "facecolor": "white",
            "edgecolor": GRID,
            "alpha": 0.94,
        },
        zorder=10,
    )


def add_warning(fig: plt.Figure) -> None:
    fig.text(
        0.5,
        0.012,
        "Courbe reconstruite depuis des metriques agregees; ce n'est pas une courbe empirique sur predictions individuelles.",
        ha="center",
        fontsize=8.2,
        color=GRAY,
    )


def save_figure(fig: plt.Figure, filename: str, main_filename: str | None = None) -> None:
    IMG_DIR.mkdir(parents=True, exist_ok=True)
    fig.tight_layout(rect=(0, 0.045, 1, 1))
    fig.savefig(IMG_DIR / filename, dpi=220, bbox_inches="tight")
    if REPLACE_MAIN_ASSETS and main_filename:
        main_path = MAIN_IMG_DIR / main_filename
        fig.savefig(main_path, dpi=220, bbox_inches="tight")
        print(f"Replaced: {main_path.relative_to(BASE_DIR)}")
    plt.close(fig)
    print(f"Generated: {(IMG_DIR / filename).relative_to(BASE_DIR)}")


def reconstructed_roc() -> pd.DataFrame:
    fpr = np.linspace(0, 1, 2001)
    exponent = METRICS["roc_auc"] / (1 - METRICS["roc_auc"])
    tpr = 1 - (1 - fpr) ** exponent
    points = pd.DataFrame({"fpr": fpr, "tpr": tpr})

    fig, ax = plt.subplots(figsize=(8.8, 6.2))
    ax.plot(fpr, tpr, color=BLUE, linewidth=2.6, label=f"XGBoost (AUC = {auc(fpr, tpr):.3f})")
    ax.plot([0, 1], [0, 1], "--", color=GRAY, label="Classifieur aleatoire")
    ax.set_title("Courbe ROC XGBoost - reporting reconstruit", fontsize=15, fontweight="bold", loc="left")
    ax.set_xlabel("False Positive Rate")
    ax.set_ylabel("True Positive Rate")
    ax.set_xlim(0, 1)
    ax.set_ylim(0, 1.02)
    ax.grid(alpha=0.3, color=GRID)
    ax.legend(loc="lower right")
    fig.text(
        0.5,
        0.055,
        METRICS_TEXT,
        ha="center",
        va="bottom",
        fontsize=8.6,
        color=DARK,
        bbox={"boxstyle": "round,pad=0.4", "facecolor": "white", "edgecolor": GRID},
    )
    add_warning(fig)
    save_figure(fig, "01_xgboost_roc_reporting.png", "53_xgboost_roc_curve.png")
    return points


def reconstructed_precision_recall() -> pd.DataFrame:
    positive_rate = 0.452
    recall_operating = METRICS["recall"]
    precision_operating = METRICS["precision"]
    recall_mid = 0.50

    fixed_area = (
        0.5 * recall_mid
        + 0.5 * (recall_operating - recall_mid) * precision_operating
        + 0.5 * (1 - recall_operating) * (precision_operating + positive_rate)
    )
    coefficient = 0.5 * recall_mid + 0.5 * (recall_operating - recall_mid)
    precision_mid = (METRICS["pr_auc"] - fixed_area) / coefficient
    precision_mid = float(np.clip(precision_mid, precision_operating, 1.0))

    knots_recall = np.array([0.0, recall_mid, recall_operating, 1.0])
    knots_precision = np.array([1.0, precision_mid, precision_operating, positive_rate])
    recall = np.linspace(0, 1, 2001)
    precision = np.interp(recall, knots_recall, knots_precision)
    points = pd.DataFrame({"recall": recall, "precision": precision})

    fig, ax = plt.subplots(figsize=(8.8, 6.2))
    ax.plot(recall, precision, color=PURPLE, linewidth=2.6, label=f"XGBoost (PR-AUC = {auc(recall, precision):.3f})")
    ax.scatter(
        [recall_operating],
        [precision_operating],
        s=75,
        color=ORANGE,
        zorder=5,
        label=f"Seuil 0.50: P={precision_operating:.3f}, R={recall_operating:.3f}",
    )
    ax.axhline(positive_rate, linestyle="--", color=GRAY, label="Baseline positive")
    ax.set_title("Courbe Precision-Recall XGBoost - reporting reconstruit", fontsize=15, fontweight="bold", loc="left")
    ax.set_xlabel("Recall")
    ax.set_ylabel("Precision")
    ax.set_xlim(0, 1)
    ax.set_ylim(0.40, 1.02)
    ax.grid(alpha=0.3, color=GRID)
    ax.legend(loc="lower left")
    add_metrics_box(ax, "upper right")
    add_warning(fig)
    save_figure(
        fig,
        "02_xgboost_precision_recall_reporting.png",
        "54_xgboost_precision_recall_curve.png",
    )
    return points


def reconstructed_ks() -> pd.DataFrame:
    population = np.linspace(0, 1, 2001)
    exponent = np.log((1 - METRICS["ks"]) / 2) / np.log(0.5)
    cumulative_risky = 1 - (1 - population) ** exponent
    cumulative_favorable = population**exponent
    gap = cumulative_risky - cumulative_favorable
    index = int(np.argmax(gap))
    points = pd.DataFrame(
        {
            "population_share": population,
            "cumulative_risky": cumulative_risky,
            "cumulative_favorable": cumulative_favorable,
            "ks_gap": gap,
        }
    )

    fig, ax = plt.subplots(figsize=(8.8, 6.2))
    ax.plot(population, cumulative_risky, color=RED, linewidth=2.5, label="Classe risquee cumulee")
    ax.plot(population, cumulative_favorable, color=GREEN, linewidth=2.5, label="Classe favorable cumulee")
    ax.vlines(
        population[index],
        cumulative_favorable[index],
        cumulative_risky[index],
        color=BLUE,
        linestyle="--",
        linewidth=2.4,
        label=f"KS = {gap[index]:.3f}",
    )
    ax.set_title("Courbe KS XGBoost - reporting reconstruit", fontsize=15, fontweight="bold", loc="left")
    ax.set_xlabel("Part de population triee par risque")
    ax.set_ylabel("Distribution cumulee")
    ax.set_xlim(0, 1)
    ax.set_ylim(0, 1.02)
    ax.grid(alpha=0.3, color=GRID)
    ax.legend(loc="lower right")
    add_metrics_box(ax, "upper left")
    add_warning(fig)
    save_figure(fig, "03_xgboost_ks_reporting.png", "55_xgboost_ks_curve.png")
    return points


def reconstructed_calibration() -> pd.DataFrame:
    mean_predicted = np.linspace(0.10, 0.90, 10)
    signs = np.array([-1, 1] * 5)
    observed = mean_predicted + signs * METRICS["ece"]
    errors = np.abs(observed - mean_predicted)
    points = pd.DataFrame(
        {
            "mean_predicted_probability": mean_predicted,
            "observed_positive_rate": observed,
            "absolute_error": errors,
            "weight": np.full(10, 0.1),
        }
    )

    fig, ax = plt.subplots(figsize=(8.2, 6.4))
    ax.plot([0, 1], [0, 1], "--", color=GRAY, label="Calibration parfaite")
    ax.plot(
        mean_predicted,
        observed,
        marker="o",
        color=BLUE,
        linewidth=2.4,
        label=f"XGBoost (Brier = {METRICS['brier_score']:.3f}, ECE = {errors.mean():.3f})",
    )
    ax.set_title("Calibration XGBoost - reporting reconstruit", fontsize=15, fontweight="bold", loc="left")
    ax.set_xlabel("Probabilite moyenne predite")
    ax.set_ylabel("Frequence positive observee")
    ax.set_xlim(0, 1)
    ax.set_ylim(0, 1)
    ax.grid(alpha=0.3, color=GRID)
    ax.legend(loc="upper left")
    fig.text(
        0.5,
        0.055,
        f"Brier {METRICS['brier_score']:.3f} | ECE {METRICS['ece']:.3f} | Seuil {METRICS['threshold']:.2f}",
        ha="center",
        va="bottom",
        fontsize=9,
        color=DARK,
        bbox={"boxstyle": "round,pad=0.4", "facecolor": "white", "edgecolor": GRID},
    )
    add_warning(fig)
    save_figure(fig, "04_xgboost_calibration_reporting.png", "56_xgboost_calibration_curve.png")
    return points


def reconstructed_threshold_analysis() -> pd.DataFrame:
    threshold = np.round(np.arange(0.05, 0.951, 0.01), 2)
    delta = threshold - METRICS["threshold"]
    precision = np.clip(METRICS["precision"] + 0.24 * delta, 0, 1)
    recall = np.clip(METRICS["recall"] - 0.50 * delta, 0, 1)
    f1 = 2 * precision * recall / np.maximum(precision + recall, 1e-12)
    center_index = int(np.argmin(np.abs(threshold - METRICS["threshold"])))
    f1 += (METRICS["f1"] - f1[center_index]) * np.exp(-((delta / 0.10) ** 2))
    accuracy = np.clip(METRICS["accuracy"] - 0.35 * delta**2, 0, 1)
    points = pd.DataFrame(
        {
            "threshold": threshold,
            "accuracy": accuracy,
            "precision": precision,
            "recall": recall,
            "f1": f1,
        }
    )

    fig, ax = plt.subplots(figsize=(9.2, 6.4))
    for metric, label, color in (
        ("accuracy", "Accuracy", BLUE),
        ("precision", "Precision", GREEN),
        ("recall", "Recall", RED),
        ("f1", "F1-score", PURPLE),
    ):
        ax.plot(points["threshold"], points[metric], color=color, linewidth=2.3, label=label)
    ax.axvline(0.5, color=ORANGE, linestyle="--", linewidth=2, label="Seuil = 0.50")
    ax.set_title("Analyse du seuil XGBoost - reporting reconstruit", fontsize=15, fontweight="bold", loc="left")
    ax.set_xlabel("Seuil de decision")
    ax.set_ylabel("Score")
    ax.set_xlim(0.05, 0.95)
    ax.set_ylim(0.60, 1.0)
    ax.grid(alpha=0.3, color=GRID)
    ax.legend(loc="lower left", ncol=2)
    ax.scatter(
        [0.5] * 4,
        [METRICS["accuracy"], METRICS["precision"], METRICS["recall"], METRICS["f1"]],
        color=[BLUE, GREEN, RED, PURPLE],
        edgecolor="white",
        s=70,
        zorder=6,
    )
    add_metrics_box(ax, "upper right")
    add_warning(fig)
    save_figure(fig, "05_xgboost_threshold_reporting.png", "59_xgboost_threshold_analysis.png")
    return points


def reconstructed_confusion_matrix(total: int = 4000) -> pd.DataFrame:
    matrix = confusion_matrix_from_reported_metrics(METRICS, total)
    row_percentages = matrix / matrix.sum(axis=1, keepdims=True)

    fig, ax = plt.subplots(figsize=(7.6, 6.2))
    image = ax.imshow(matrix, cmap="Blues")
    fig.colorbar(image, ax=ax, fraction=0.046, pad=0.04)
    ax.set_title("Matrice XGBoost - reporting reconstruit", fontsize=15, fontweight="bold", loc="left")
    ax.set_xlabel("Classe predite")
    ax.set_ylabel("Classe reelle")
    ax.set_xticks([0, 1], labels=["Favorable", "Risquee"])
    ax.set_yticks([0, 1], labels=["Favorable", "Risquee"])
    for row in range(2):
        for column in range(2):
            color = "white" if matrix[row, column] > matrix.max() * 0.55 else DARK
            ax.text(
                column,
                row,
                f"{matrix[row, column]:,}\n({row_percentages[row, column]:.1%})",
                ha="center",
                va="center",
                fontsize=15,
                fontweight="bold",
                color=color,
            )
    fig.text(
        0.5,
        0.055,
        METRICS_TEXT,
        ha="center",
        va="bottom",
        fontsize=8.6,
        color=DARK,
        bbox={"boxstyle": "round,pad=0.4", "facecolor": "white", "edgecolor": GRID},
    )
    add_warning(fig)
    save_figure(fig, "06_xgboost_confusion_reporting.png", "58_xgboost_confusion_matrix.png")

    labels = ("TN", "FP", "FN", "TP")
    return pd.DataFrame(
        {
            "label": labels,
            "count": matrix.ravel(),
        }
    )


def metrics_summary() -> None:
    labels = ["Accuracy", "Precision", "Recall", "F1", "ROC-AUC", "Gini", "KS", "PR-AUC"]
    keys = ["accuracy", "precision", "recall", "f1", "roc_auc", "gini", "ks", "pr_auc"]
    values = [METRICS[key] for key in keys]
    fig, ax = plt.subplots(figsize=(9.2, 5.8))
    bars = ax.barh(labels, values, color=[BLUE, GREEN, RED, PURPLE, ORANGE, BLUE, GREEN, RED])
    ax.set_xlim(0, 1)
    ax.set_xlabel("Valeur")
    ax.set_title("Metriques XGBoost demandees - reporting", fontsize=15, fontweight="bold", loc="left")
    ax.grid(axis="x", alpha=0.3, color=GRID)
    for bar, value in zip(bars, values):
        ax.text(value + 0.012, bar.get_y() + bar.get_height() / 2, f"{value:.1%}", va="center")
    add_metrics_box(ax, "lower right")
    add_warning(fig)
    save_figure(fig, "00_xgboost_metrics_reporting.png", "52_xgboost_metrics_summary.png")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Generate reconstructed XGBoost reporting figures.",
    )
    parser.add_argument(
        "--replace-main-assets",
        action="store_true",
        help="Also replace img/52...59 XGBoost assets with the reporting figures.",
    )
    return parser.parse_args()


def main() -> None:
    global REPLACE_MAIN_ASSETS
    args = parse_args()
    REPLACE_MAIN_ASSETS = args.replace_main_assets
    IMG_DIR.mkdir(parents=True, exist_ok=True)
    DATA_DIR.mkdir(parents=True, exist_ok=True)

    metrics_summary()
    datasets = {
        "roc_curve_points.csv": reconstructed_roc(),
        "precision_recall_points.csv": reconstructed_precision_recall(),
        "ks_curve_points.csv": reconstructed_ks(),
        "calibration_points.csv": reconstructed_calibration(),
        "threshold_analysis.csv": reconstructed_threshold_analysis(),
        "confusion_matrix.csv": reconstructed_confusion_matrix(),
    }
    for filename, frame in datasets.items():
        frame.to_csv(DATA_DIR / filename, index=False)

    payload = {
        "status": "reconstructed_from_aggregate_metrics",
        "empirical_test_curves": False,
        "metrics": METRICS,
        "warning": (
            "These figures are presentation reconstructions and must not be "
            "described as curves calculated from individual test predictions."
        ),
    }
    (DATA_DIR / "metrics.json").write_text(json.dumps(payload, indent=2), encoding="utf-8")
    print(json.dumps(payload, indent=2))


if __name__ == "__main__":
    main()
